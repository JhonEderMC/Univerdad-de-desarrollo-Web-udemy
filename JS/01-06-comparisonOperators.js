let a = 3, b = 2 , c = "3";

console.log(`a : ${a}`);
console.log(`b : ${b}`);
console.log(`c : "${c}"`);

console.log(`a == b : ${a == b}`); 
console.log(`a == c : ${ a == c}`); // the value is checked regardless of the type
console.log(`a === b : ${a === b}`);
console.log(`a === c : ${ a === c}`); // the value is checked importing the type